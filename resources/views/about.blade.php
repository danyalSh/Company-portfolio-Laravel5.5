								<h2 class="colorlib-heading">Who we are</h2>
								<p>ACE touch, c’est l’histoire d’une rencontre entre un grand chef d’entreprise évoluant sur la scène internationale et un architecte, belges tous deux. Les complices se connaissaient mais ne s’étaient plus vus depuis plus de 15 ans. C’est au bord de la mer méditerranée, turquoise, un été sur la côte africaine que lors de ces retrouvailles, l’idée a germé. L’évidence que l’alliance de leur complémentarité était une chance à ne pas laisser passer, les amena à s’associer afin de créer ACE touch.
ACE touch a pour finalité de répondre à tous les besoins de ses clients en ce qui concerne la création d’images 3D, d’animations 3D, de vidéos, de design d’objets en tout genre</p>
<p>
 ACE touch met en œuvre des médias 3D hyperréalistes pour les domaines d’activité les plus variés, l’architecture, la promotion immobilière, l’industrie, le commerce, la culture, … :
-	Architecture 3D – lien vers portfolio - : Nous réalisons des médias 3D fixes ou animés, afin de mettre en scène vos produits immobiliers, selon vos volontés, mais avec notre grande expertise et le talent nécessaire afin de vous livrer des visuels pointus, détaillés, précis et donc crédibles et vendeurs. Nous réalisons également, des maquettes numériques REVIT-BIM pour tous types de projets.
-	3D Didactique – lien vers portfolio - : Vous êtes un industriel ou un commerçant, vous avez élaboré un produit et/ou vous vendez un produit. Vous pensez à juste titre que nous pourrions être un atout majeur dans le développement de votre entreprise, c’est le cas. Nous réalisons des visuels 3D et des animations didactiques, présentant vos produits, et expliquant le fonctionnement et la technique de ces derniers. Nos différents médias vous permettront également d’expliquer à vos clients ou vos collaborateurs, les techniques de montage et de mise en œuvre de vos matériels.</p>
<p>
-	Visualisation produits 3D – lien vers portfolio - : Nous réalisons vos catalogues, des images photo-réalistes et des fiches produits. Nous mettons en œuvre des vidéos 3D, des publicités, afin de présenter, de mettre en valeur et de vendre vos créations.
-	Design produits 3D – lien vers portfolio - : Notre équipe de designer, en total respect de vos désidérata, vous conseillera et réalisera un design raffiné, cohérent, fonctionnel et vendeur en vue de mettre en œuvre vos créations et vos idées.
-	High-Tech – lien vers portfolio – nous mettons en scène vos produits, en utilisant les dernières solutions les plus avant-gardistes, projections mapping, hologramme, relief, …, afin de mettre en valeur vos productions de toutes sortes.
En parallèle, dans une logique de complémentarité, ACE touch développe un Bureau d’Etudes Techniques pluridisciplinaires, ACE.BET pour accompagner les divers intervenants du BTP dans l’édification de leurs projets, et également une structure de développeurs internet, ACE.NET afin de réaliser des sites internet pour les architectes, les BET du BTP, ainsi que les promoteurs immobiliers.
En conclusion, ACE touch est au service de votre entreprise afin de mettre en avant vos produits quels qu’ils soient, notre but est que nos clients deviennent les leaders dans leur domaine d’activité. Nous voulons vous ouvrir de nouveaux horizons en accord avec notre maxime « le virtuel au service du réel » et mieux encore, « notre virtuel au service de votre réel ». Nous sommes fiers de partager les mêmes ambitions de réussites et d’excellences que les vôtres.
</p>
